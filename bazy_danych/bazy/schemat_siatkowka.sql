create table siatkowka.siatkarki (
  numer smallint not null,
  iddruzyny varchar(5) not null references siatkowka.druzyny,
  imie varchar(12) not null,
  nazwisko varchar(30) not null,
  pozycja varchar(12) not null,
  primary key (numer, iddruzyny)
);

create table siatkowka.mecze (
  idmeczu smallint primary key,
  gospodarze varchar(5) not null references siatkowka.druzyny,
  goscie varchar(5) not null references siatkowka.druzyny,
  termin date not null
);

create table siatkowka.statystyki (
  idmeczu smallint primary key references siatkowka.mecze,
  gospodarze smallint[],
  goscie smallint[]
);

create table siatkowka.punktujace (
  numer smallint not null,
  iddruzyny varchar(5) not null references siatkowka.druzyny,
  idmeczu smallint not null references siatkowka.mecze,
  punkty smallint not null,
  primary key (numer, iddruzyny, idmeczu),
  foreign key (numer, iddruzyny) references siatkowka.siatkarki
);


